package io.macgyver.core;

public class MultiToolException extends RuntimeException {

	public MultiToolException() {
		super();
		
	}

	public MultiToolException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public MultiToolException(String arg0) {
		super(arg0);
		
	}

	public MultiToolException(Throwable arg0) {
		super(arg0);
		
	}

}
