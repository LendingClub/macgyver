package io.macgyver.core;

public class ServiceInvocationException extends MacGyverException {

	public ServiceInvocationException() {
		super();
	}

	public ServiceInvocationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ServiceInvocationException(String arg0) {
		super(arg0);
	}

	public ServiceInvocationException(Throwable arg0) {
		super(arg0);
	}

}
